# Data Types in Python
# Integer
a = 10
print("Integer:", a)

# Float
b = 10.5
print("Float:", b)

# String
c = "Hello"
print("String:", c)
print("String:", c)

# Boolean
d = True
print("Boolean:", d)

#Multiple Assignment
x, y, z = 1, 2.5, "Python"
print("Multiple Assignment:", x, y, z)

# Chained Assignment
a=b=c=100
print("Chained Assignment:", a, b, c)

# Type Conversion
num_str = "123" # "String"
num_int = int(num_str)
print("Type Conversion from String to Integer:", num_int)

# Swapping Variables
m = 5
n = 10
m, n = n, m
print("Swapped Variables:", m, n)

# Lists
my_list = [1,10, 2, 30, 30, 5,9]

print(my_list[3])
my_list.append(50)
my_list.remove(1)
print("List:", my_list)

# Sets
print("====================================")
